export const getGoogleDriveImageUrl = (urlOrId: string): string => {
  if (!urlOrId) return '';

  // If it's already just a file ID, return it; otherwise, extract ID from URL
  const idMatch = urlOrId.match(/[-\w]{25,}/);
  if (!idMatch) return ''; // invalid input

  const fileId = idMatch[0];
  return `https://drive.google.com/uc?export=view&id=${fileId}`;
};
export const extractFileId = (url: string): string => {
  if (!url) return '';
  const idMatch = url.match(/[-\w]{25,}/); // file IDs are usually 25+ chars
  return idMatch ? idMatch[0] : url;
};
